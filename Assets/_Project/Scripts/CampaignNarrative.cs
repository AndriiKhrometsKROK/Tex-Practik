    public static string GetHubStory()
    {
        if (CampaignProgress.Act2Completed)
        {
            return "Петлю зламано.\n\nУламки замку більше не повертаються на старі місця. У темряві залишився шлях до наступного акту — і короткий образ жінки, яку Ке́нам шукав.";
        }

        int level = CampaignProgress.HighestUnlocked;
        if (level >= 26)
        {
            return "Світ перестав приховувати свою штучність.\n\nНаступний цикл готується порушити власні правила. Демон майже втратив форму, але продовжує чекати.";
        }
        if (level >= 20)
        {
            return "Демон більше не здається недосяжним.\n\nТепер між хвилями йому можна кинути виклик. Чим довше триває гра, тим слабшим стає її Смотритель.";
        }
        if (level >= 10)
        {
            return "Цикли повторюються надто точно.\n\nКе́нам пам'ятає удари, яких ніколи не мав пам'ятати. У бібліотеці з'явилися записи, яких раніше не існувало.";
        }

        return "Ке́нам прокинувся серед уламків замку, якого немає на жодній мапі.\n\nБібліотека пам'ятає знищені світи. Демон пам'ятає кожну поразку. Ворота ведуть у наступний цикл.";
    }

    public static string GetDemonDialogue()
    {
        if (CampaignProgress.Act2Completed)
            return "«Правила зламані. Та не думай, що за цією грою не було іншої.»";
        if (CampaignProgress.HighestUnlocked >= 26)
            return "«Ще один цикл. Можливо, цього разу програє не лише твій замок.»";
        if (CampaignProgress.HighestUnlocked >= 20)
            return "«Тепер ти можеш викликати мене. Питання лише в тому, скільки моєї сили ти готовий витримати.»";
        if (CampaignProgress.HighestUnlocked >= 10)
            return "«Ти почав помічати повторення. Це мине після наступної поразки. Напевно.»";
        return "«Ти знову повернувся. Це наполегливість чи нездатність зрозуміти поразку?»";
    }
